package com.hkdemircan._02.concurrentCollections;

public class Notes {

    // The java.util.concurrent package provides a number of classes that are thread-safe equivalents
    // of the ones provided in the collections framework classes in the java.util package.
    // For example, java.util.concurrent.ConcurrentHashMap is a concurrent equivalent to java.util.HashMap.
    // The main difference between these two containers is
    // that you need to explicitly synchronize insertions and deletions with HashMap,
    // whereas such synchronization is built into the ConcurrentHashMap.
}
